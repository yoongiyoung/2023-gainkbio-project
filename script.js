document.getElementById("button1").addEventListener("mouseover", function() {
    this.style.opacity = 0.5; // 마우스를 올리면 반투명해집니다.
    this.style.backgroundColor = 'rgba(99, 99, 99, 0.5)'; // 추가된 코드
});

document.getElementById("button1").addEventListener("mouseout", function() {
    this.style.opacity = 0; // 마우스를 내리면 투명해집니다.
    this.style.backgroundColor = 'rgba(99, 99, 99, 0)'; // 추가된 코드
});

